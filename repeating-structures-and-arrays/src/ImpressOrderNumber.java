public class ImpressOrderNumber {
    public static void main(String[] args) {
        int numbers;

        for (int i = 0; i <= 20; i++) {
            numbers = i;
            System.out.println("Number : "+ numbers);            
        }

        for (int i = 0; i <= 20; i++) {
            numbers = i;
            System.out.print( numbers + " ");            
        }
        System.out.println();
    }
}

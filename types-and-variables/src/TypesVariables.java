public class TypesVariables {
    public static void main(String[] args) throws Exception {

        //Primitive Types
        //VARIABLES
        double area = 200.3;
        int number = 1000;
        String name = "Jean Santos";
        boolean positive = true;
        System.out.println(area +" "+ number+" "+ name +" "+ positive);

        //CONSTANTS
        final double VALU_OF_PI = 3.1416;
        System.out.println(VALU_OF_PI);
    }
}
